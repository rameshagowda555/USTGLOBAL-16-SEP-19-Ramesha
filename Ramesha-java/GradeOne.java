class GradeOne
{
	public static void main(String[] args)
	{
		int grade = 'A';
		if(grade == 'A')
		{
		System.out.println("Fcd");
		}
		else if(grade == 'B')
		{
		System.out.println("Fc ");
		}
		else if(grade == 'C')
		{
		System.out.println("Sc ");
		}
		else if(grade == 'D')
		{
		System.out.println(" Just Pass ");
		} 
		else if(grade == 'E')
		{       
		System.out.println("Fail");
		}
		else{          
		System.out.println("Not Matched");
		         
	 	}
	}
}